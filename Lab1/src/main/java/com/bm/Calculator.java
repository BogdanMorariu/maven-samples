class Calculator {

    protected int add(int a, int b){
        return a+b;
    }

    protected int subtract(int a, int b){
        return a-b;
    }

    protected int multiply(int a, int b){
        return a*b;
    }

    protected float divide(int a, int b){
        return a/b;
    }
}
